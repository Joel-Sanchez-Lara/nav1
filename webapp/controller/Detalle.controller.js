sap.ui.define([
	"sap/ui/core/mvc/Controller",
	"sap/ui/core/routing/History"
	], function (Controller,History) {
	"use strict";
	return Controller.extend("com.nav1.nav1.controller.Detalle", {
		/**
		 * Called when a controller is instantiated and its View controls (if available) are already created.
		 * Can be used to modify the View before it is displayed, to bind event handlers and do other one-time initialization.
		 * @memberOf com.nav1.nav1.view.Detalle
		 */
		onInit: function () {
			//Variable para router
			var oRouter = sap.ui.core.UIComponent.getRouterFor(this);
			oRouter.getRoute("detalle").attachPatternMatched(this._onRouteFound, this);
		},
		_onRouteFound: function (oEvent) {
				var oArgument = oEvent.getParameter("arguments");
				var oView = this.getView();
				//Hacemos el bindinng del argumento pasado por la URI y lo dejamos en un modelo 
				oView.bindElement({
					path: "/datos('" + oArgument.rutPath + "')"
				});
			}
			/**
			 * Similar to onAfterRendering, but this hook is invoked before the controller's View is re-rendered
			 * (NOT before the first rendering! onInit() is used for that one!).
			 * @memberOf com.nav1.nav1.view.Detalle
			 */
			//	onBeforeRendering: function() {
			//
			//	},
			/**
			 * Called when the View has been rendered (so its HTML is part of the document). Post-rendering manipulations of the HTML could be done here.
			 * This hook is the same one that SAPUI5 controls get after being rendered.
			 * @memberOf com.nav1.nav1.view.Detalle
			 */
			//	onAfterRendering: function() {
			//
			//	},
			/**
			 * Called when the Controller is destroyed. Use this one to free resources and finalize activities.
			 * @memberOf com.nav1.nav1.view.Detalle
			 */
			//	onExit: function() {
			//
			//	}
			,
		/**
		 *@memberOf com.nav1.nav1.controller.Detalle
		 */
		onVolver: function (oEvent) {
			//This code was generated by the layout editor.
			var oHistory = History.getInstance();
			var sPreviousHash = oHistory.getPreviousHash();
			
			//Validar que no venga al link directo
			if(sPreviousHash !== undefined){
				window.history.go(-1);
			}else{
				var oRouter = sap.ui.core.UIComponent.getRouterFor(this);
				oRouter.navTo("resumen", {}, true);
			}

			
		}
	});
});